export type NavItem = {
  label: string;
  href: string;
  icon: string;
  section?: string;
};

export type StatusTone = 'success' | 'warning' | 'danger' | 'info' | 'neutral' | 'purple';

export type FormField = {
  label: string;
  key: string;
  type?: 'text' | 'number' | 'select' | 'textarea';
  required?: boolean;
  options?: string[];
};

export type PageConfig = {
  title: string;
  eyebrow: string;
  description: string;
  primaryAction: string;
  api: string[];
  columns: string[];
  rows: Array<Record<string, string | number>>;
  emptyState: string;
  formFields: string[];
  tabs?: string[];
  listEndpoint: string;
  rowMapper: (item: Record<string, unknown>) => Record<string, string>;
  createEndpoint?: string;
  createFields?: FormField[];
  deleteEndpoint?: (id: string) => string;
  createNote?: string;
};

function fmt(v: unknown): string {
  if (v == null || v === '') return '—';
  return String(v);
}

function fmtMoney(v: unknown): string {
  if (v == null || v === '') return '—';
  return `£${Number(v).toFixed(2)}`;
}

function fmtDate(v: unknown): string {
  if (!v) return '—';
  return new Date(v as string).toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric' });
}

